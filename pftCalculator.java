    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int pass=1;
        int score=0;
        int situp=Integer.parseInt(jTextField2.getText());
        int pullup=Integer.parseInt(jTextField1.getText());
        int min=Integer.parseInt(jTextField3.getText());
        int sec=Integer.parseInt(jTextField4.getText());;
        if(jRadioButton1.isSelected()){
            if(situp>100){
                situp=100;
            }
            if(pullup>20){
                pullup=20;
            }
            int total = min*60+sec;
            int temp = 100-(total-1080)/10;
            if(temp>100){
                score+=100;
            }
            else if(min>=28) pass= 0;
            else{
                score+=temp;
            }
            score += situp;
            score += pullup*5;
    }
        else{
            if(situp>100){
                situp=100;
            }
            if(pullup>40){
                if(pullup>70){
                    score+=100;
                    pullup=0;
                }
                else{
                score+=40;
                pullup-=40;
                score+=pullup*2;
                }
            }
            else{
                score+=pullup;
            }
            
            int total = min*60+sec;
            int temp = 100-(total-1260)/10;
            if(temp>100){
                score+=100;
            }
            else if(min>=31) pass= 0;
            else{
                score+=temp;
            }
            score += situp;
            
        }
        if(pass == 0) {
            jTextField5.setText("FAIL");
            jTextField5.setBackground(Color.red);
        }
        else{
            jTextField5.setText(""+score);
            jTextField5.setBackground(Color.green);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

